        global currentSentence
        words=currentSentence.lower().split(" ")
        if words:
            if words[0]=="":
                self.textBrowser_3.setText(_translate("MainWindow", "", None))
            elif words[-1]=="":
                predWord=autocomplete.predict('', words[-2])
                if predWord:
                    predWord=predWord[0][0]
